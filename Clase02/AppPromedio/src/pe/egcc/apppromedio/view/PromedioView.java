  private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
    // Datos
    int nota1 = Integer.parseInt( txtNota1.getText() );
    int nota2 = Integer.parseInt( txtNota2.getText() );
    int nota3 = Integer.parseInt( txtNota3.getText() );
    int nota4 = Integer.parseInt( txtNota4.getText() );
    
    // Proceso
    PromedioService promedioService = new PromedioService();
    int notaMenor = promedioService.notaMenor(nota1, nota2, nota3, nota4);
    int promedio = promedioService.calcularPromedio(nota1, nota2, nota3, nota4);
    String condicion = promedioService.obtenerCondicion(promedio);
    
    // Reporte
    txtNotaMenor.setText("" + notaMenor);
    txtPromedio.setText("" + promedio);
    txtCondicion.setText("" + condicion);
    
    // Controles
    txtNota1.setEnabled(false);
    txtNota2.setEnabled(false);
    txtNota3.setEnabled(false);
    txtNota4.setEnabled(false);
    btnProcesar.setEnabled(false);
    btnLimpiar.setEnabled(true);
  }//GEN-LAST:event_btnProcesarActionPerformed

